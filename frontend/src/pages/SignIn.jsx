// import React from 'react'
import React, { useContext, useState } from 'react';
import bg from "../assets/authBg.png"
import { IoMdEye } from "react-icons/io";
import { IoMdEyeOff } from "react-icons/io";
import { useNavigate } from 'react-router-dom';
import { userDataContext } from '../context/userContext';
import axios from "axios";

function SignIn(){
  const[showPassword,setShowPassword]=useState(false)
  const {userData,setUserData}=useContext(userDataContext)
  const navigate = useNavigate()
  
    const[email,setEmail]=useState("")
    const [loading,setLoading]=useState(false)
    const[password,setPassword]=useState("")
    const[err,setErr]=useState("")

      const handleSignIn=async (e)=>{
        e.preventDefault()
        setErr("") 
        setLoading(true)
        try{
          const result = await axios.post(`/api/auth/login`, { email, password }, { withCredentials: true });
           
            if (result.data.token) {
      localStorage.setItem("token", result.data.token);
    }

       if (result.data.user) {
  setUserData(result.data.user);
}
          setLoading(false)
          navigate("/")
        }catch (error){
          console.log(error)
          setLoading(false)
          setUserData(null)
           setErr(error.response?.data?.message || "login failed")
        }
      }
  return(
   <div className='w-full h-[100%] bg-cover flex justify-center items-center' style={{backgroundImage:`url(${bg})`}}>

      <form className='w-[90%] h-[500px] max-w-[450px] bg-[#00000062] backdrop-blur shadow -lg shadow-black flex flex-col item-center justify-center gap-[20px] px-[20px]' onSubmit={handleSignIn}>
      <h1 className='text-white text-[30px] font-semibold mb-[30px]'>Sign In to <span className='text-blue-400'>Virtual Assistant</span></h1>

       <input type="email" id="email"  name="email" placeholder='Email'className='w-full h-[60px] outline-none border-2 border-white bg-transparent text-white placeholder-gray-300 px-[20px] py-[10px] rounded-full text-[18px]'required onChange={(e)=>setEmail(e.target.value)} value={email}/>
       <div className='w-full h-[60px] border-2 border-white bg-transparent text-white rounded-full text-[18px] relative'>
        <input type={showPassword?"text":"password"} placeholder='password' className='w-full h-full rounded-full outline-none bg-transparent placeholder-gray-300 px-[20px] py-[10px] ' required onChange={(e)=>setPassword(e.target.value)} value={password}/>

          {!showPassword && <IoMdEye className='absolute top-[18px] right-[20px] w-[25px] h-[25px] text-[white] cursor-pointer' onClick={()=>setShowPassword(true)}/> 
      }

        {showPassword && <IoMdEyeOff className='absolute top-[18px] right-[20px] w-[25px] h-[25px] text-[white] cursor-pointer' onClick={()=>setShowPassword(false)}/> 
 } 
         </div>
         {err.length>0 && <p className='text-red-500 text-[170x]'>*{err}</p>}
         <button type="submit" className='min-w-[150px] h-[60px] mt-[30px] text-black font-semibold bg-white rounded-full text-[19px]' disabled={loading}>{loading?"Loading...":"Sign In"}</button>
        <p className='text-white text-[18px] cursor-pointer' onClick={ ()=>navigate("/signup")}>Want to create a new account ?<span className='text-blue-400'>Sign Up</span></p>
      </form>
   </div>

    
  )
}

export default SignIn

    
  